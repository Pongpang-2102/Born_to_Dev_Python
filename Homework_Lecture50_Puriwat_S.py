def Add(x,y):
    print("This is Add Function")
    print(x,"+",y,"=",x+y)
    print("-----------")
def Deduct(x,y):
    print("This is Deduct Function")
    print(x,"-",y,"=",x-y)
    print("-----------")
def Multiply(x,y):
    print("This is Multiply Function")
    print(x,"x",y,"=",x*y)
    print("-----------")
def Devide(x,y):
    print("This is Devide Function")
    print(x,"/",y,"=",x/y)
    print("-----------")

def Combine (x,y):
    print("This is Combine (+-*/) Function")
    print(x, "+", y, "=", x + y)
    print(x, "-", y, "=", x - y)
    print(x, "x", y, "=", x * y)
    print(x, "/", y, "=", x / y)
    print("-----------")

Add(3,7)
